                    when questioned about wrongdoing.
                </li>
                <li>
                    <strong>Have integrity.</strong>&nbsp;Any homework, reports, exams, tests,
                    or other graded or ungraded work is to be the sole work of the student
                    whose name appears on that material.
                    He/she does not claim another&rsquo;s work as his/her own without giving
                    proper credit.
                </li>
                <li>
                    <strong>Shall not steal.</strong>&nbsp;He/she does not take or use what
                    belongs to another without being granted prior permission.
                </li>
                <li>
                    <strong>Uphold the Honor Code.</strong>&nbsp;Through charitable fraternal
                    correction, he or she does not knowingly tolerate another student&rsquo;s
                    violation of the Honor Code.
                </li>
                <li>
                    <strong>Follow a respectful social media persona.</strong>&nbsp;This
                    includes texting, commenting, and photos when using apps like Instagram,
                    YouTube, Snapchat, or any other social
                    media platform. Actions and words should not be harmful to oneself, BFC
                    community, or anyone else.&nbsp;
                </li>
            </ul>
